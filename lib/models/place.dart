class Place {
  final int id;
  final String country;
  final String state;
  final String name;
  final String city;
  final int price;
  final List<String> images;
  final int maxCapacity;
  final double rate;
  final double latitude;
  final double longitude;
  final int numberOfBedrooms;

  Place({
    this.id,
    this.country,
    this.state,
    this.city,
    this.price,
    this.images,
    this.maxCapacity,
    this.rate,
    this.name,
    this.latitude,
    this.longitude,
    this.numberOfBedrooms,
  });



}
